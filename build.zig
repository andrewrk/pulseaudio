        .HAVE_GETPWUID_R = 1,
        .HAVE_GETTIMEOFDAY = 1,
        .HAVE_GETUID = 1,
        .HAVE_GRP_H = 1,
        .HAVE_HAL_COMPAT = 1,
        .HAVE_ICONV = 1,
        .HAVE_IPV6 = 1,
        .HAVE_LANGINFO_H = 1,
        .HAVE_LINUX_SOCKIOS_H = 1,
        .HAVE_LOCALE_H = 1,
        .HAVE_LRINTF = 1,
        .HAVE_LSTAT = 1,
        .HAVE_MEMFD = 1,
        .HAVE_MEMFD_CREATE = 1,
        .HAVE_MKFIFO = 1,
        .HAVE_MLOCK = 1,
        .HAVE_NANOSLEEP = 1,
        .HAVE_NETDB_H = 1,
        .HAVE_NETINET_IN_H = 1,
        .HAVE_NETINET_IN_SYSTM_H = 1,
        .HAVE_NETINET_IP_H = 1,
        .HAVE_NETINET_TCP_H = 1,
        .HAVE_OPEN64 = 1,
        .HAVE_OSS_OUTPUT = 1,
        .HAVE_OSS_WRAPPER = 1,
        .HAVE_PIPE = 1,
        .HAVE_PIPE2 = 1,
        .HAVE_POLL_H = 1,
        .HAVE_POSIX_FADVISE = 1,
        .HAVE_POSIX_MADVISE = 1,
        .HAVE_POSIX_MEMALIGN = 1,
        .HAVE_PPOLL = 1,
        .HAVE_PTHREAD = 1,
        .HAVE_PTHREAD_GETNAME_NP = 1,
        .HAVE_PTHREAD_PRIO_INHERIT = 1,
        .HAVE_PTHREAD_SETAFFINITY_NP = 1,
        .HAVE_PTHREAD_SETNAME_NP = 1,
        .HAVE_PWD_H = 1,
        .HAVE_READLINK = 1,
