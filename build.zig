            "src/pulsecore/socket-client.c",
            "src/pulsecore/socket-server.c",
            "src/pulsecore/socket-util.c",
            "src/pulsecore/srbchannel.c",
            "src/pulsecore/strbuf.c",
            "src/pulsecore/strlist.c",
            "src/pulsecore/tagstruct.c",
            "src/pulsecore/thread-posix.c",
            "src/pulsecore/time-smoother.c",
            "src/pulsecore/tokenizer.c",
            "src/pulsecore/usergroup.c",
        },
        .flags = &.{
            "-std=gnu11",
            "-DHAVE_CONFIG_H=1",
            "-D_GNU_SOURCE",
        },
    });
    lib.installHeadersDirectory(b.path("src/pulse"), "pulse", .{
        .exclude_extensions = &.{ ".c", ".h.in" },
    });
    lib.installConfigHeader(version_header);
    b.installArtifact(lib);

    const bindings = b.addModule("pulseaudio", .{
        .root_source_file = b.path("bindings.zig"),
        .target = target,
        .optimize = optimize,
    });
    bindings.linkLibrary(lib);

    const sine_exe = b.addExecutable(.{
        .name = "sine",
        .root_module = b.createModule(.{
            .root_source_file = b.path("example/sine.zig"),
            .target = target,
            .optimize = optimize,
            .imports = &.{
                .{
                    .name = "pulseaudio",
                    .module = bindings,
                },
            },
        }),
    });
    b.installArtifact(sine_exe);
}

fn have(b: bool) ?c_int {
    if (b) {
        return 1;
    } else {
        return null;
    }
}
