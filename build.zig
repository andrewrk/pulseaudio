        .HAVE_SETRESUID = 1,
        .HAVE_SETREUID = 1,
        .HAVE_SETSID = 1,
        .HAVE_SHM_OPEN = 1,
        .HAVE_SIGACTION = 1,
        .HAVE_SIGXCPU = 1,
        .HAVE_STDINT_H = 1,
        .HAVE_STD_BOOL = 1,
