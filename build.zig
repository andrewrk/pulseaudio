        .PA_BINARY = "/usr/local/bin/pulseaudio",
        .PA_BUILDDIR = "/home/pulseaudio/build",
        .PA_CFLAGS = "Not yet supported on meson",
        .PA_DEFAULT_CONFIG_DIR = "/usr/local/etc/pulse",
        .PA_DLSEARCHPATH = "/usr/local/lib/pulse-16.1/modules",
